function out = F1(xx,yy)
  if (xx > 1.0 || xx < 0.0 || yy > 1.0 || yy < 0.0)
    out = 0;
  else
    f1Min = -0.197779023177334;
    f1Len = 1.23923657440914;
    out = 3./4.*exp(-1./4.*((9.*xx -2).^2+(9.*yy-2).^2))+ 3./4.*exp(-1./49.*(9.*xx + 1).^2 - 1./10.*(9.*yy + 1).^2)+1./2.*exp(-1./4.*(9.*xx - 7).^2 - 1./4.*(9.*yy - 3).^2)-1./5.*exp(-(9.*xx - 4).^2 - (9.*yy - 7).^2);
    out = (out .- f1Min) / f1Len;
  end
end
